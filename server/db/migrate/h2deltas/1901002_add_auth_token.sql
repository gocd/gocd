CREATE TABLE AuthToken (
id             BIGINT GENERATED BY DEFAULT AS IDENTITY (START WITH 1) PRIMARY KEY,
name           VARCHAR(255) Not NULL,
value          VARCHAR(255) UNIQUE,
description    VARCHAR(1024),
isRevoked      BOOLEAN,
createdAt      TIMESTAMP,
lastUsed       TIMESTAMP,
username       VARCHAR(255) Not NULL,
);

