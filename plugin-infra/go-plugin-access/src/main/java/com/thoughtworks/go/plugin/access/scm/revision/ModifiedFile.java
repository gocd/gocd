package com.thoughtworks.go.plugin.access.scm.revision;

import java.io.Serializable;

public class ModifiedFile implements Serializable {
    private String fileName;
    private ModifiedAction action = ModifiedAction.unknown;

    public ModifiedFile() {
    }

    public ModifiedFile(String fileName, ModifiedAction action) {
        this.fileName = fileName;
        this.action = action;
    }

    public String getFileName() {
        return fileName;
    }

    public void setFileName(String fileName) {
        this.fileName = fileName;
    }

    public ModifiedAction getAction() {
        return action;
    }

    public void setAction(ModifiedAction action) {
        this.action = action;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;

        ModifiedFile that = (ModifiedFile) o;

        if (action != that.action) return false;
        if (fileName != null ? !fileName.equals(that.fileName) : that.fileName != null) return false;

        return true;
    }

    @Override
    public int hashCode() {
        int result = fileName != null ? fileName.hashCode() : 0;
        result = 31 * result + (action != null ? action.hashCode() : 0);
        return result;
    }
}
