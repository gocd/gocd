package com.thoughtworks.go.config;

import java.io.Serializable;

/**
 * @understands encrypted values for env variables
 */
@ConfigTag("encryptedValue")
public class EncryptedVariableValueConfig implements Serializable{
    @ConfigValue
    private String value;

    public EncryptedVariableValueConfig() {
    }

    public EncryptedVariableValueConfig(String value) {
        this.value = value;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) {
            return true;
        }
        if (o == null || getClass() != o.getClass()) {
            return false;
        }

        EncryptedVariableValueConfig that = (EncryptedVariableValueConfig) o;

        if (value != null ? !value.equals(that.value) : that.value != null) {
            return false;
        }

        return true;
    }

    @Override
    public int hashCode() {
        return value != null ? value.hashCode() : 0;
    }

    public String getValue() {
        return value;
    }
}
